&Uuml;ber die Schulessenseite:<br><p>
Die Idee f&uuml;r diese Seite kam mir etwa gegen November 2013.
Die Tatsche, dass man mit der Bezeichnung der angebotenen Essen vom Men&uuml;service Neuburg
am GHG-Wismar fast nichts anfangen kann, das Essen meist auch sehr interessant aussieht, auf der Website
des Men&uuml;service nirgends Bilder vom Essen zu finden waren und
Alwin aus der 11b anfing, Bilder vom Schulessen zu machen, um seinen Eltern zu zeigen, was vom Essenanbieter
geliefert wird, haben mich auf die Idee gebracht "mal schnell" eine Datenbank mit Bilder anzulegen, eine Website zu bauen
und Jedem zu erm&ouml;glichen, diese Bilder anzusehen, das Essen zu bewerten und selbst Bilder vom Essen zu machen
und hochzuladen auf den <a href="http://ldkf.de" style="color: #2E64FE;" >LDKF-Server</a>.
Au&szlig;erdem kann man auf der Seite den Plan des Aktuellen Monats durchsehen und mithilfe der Bilder und Bewertungen
leichter Entscheiden, was man <a href="http://www.menue-service-neuburg.de/" style="color:#2E64FE;" title="Men&uuml;-Service-Neuburg">bestellen</a> m&ouml;chte.<br>
Mitte Februar 2014 fing ich dann auch schon an, nachdem ich eine Tabelle auf wei&szlig;em Hintergrund, eine leere Datenbank und 
eine nicht ganz leicht zu verstehende Navigation hatte, wurden einige Test-Bilder hochgeladen.
Danach hab ich an einem Wochenende das Design ge&auml;ndert, das sich an dem der Startseite von ldkf.de orientierte.
Zu dem Zeitpunkt wurde auch die Navigation auf der Seite vollkommen &uuml;berarbeitet sowie das Bewertungssystem entwickelt 
und umgesetzt.
Danach folgte eine weitere Design&auml;nderung, sowie einige weitere Verbesserungen, mitunter in der Navigation, sowie ein verbessertes Skript zum einlesen des neuen Essenplans.
</p>
